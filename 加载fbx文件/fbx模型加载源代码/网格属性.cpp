void C网格属性::添加元素(S属性* 属性)
{
    元素.resize(元素.size() + 1);
    元素[元素.size() - 1] = 属性;
}

ComPtr<ID3D12Resource> C网格属性::贴图(UINT 索引)
{
    return 纹理集[元素[索引]->纹理索引];
}

